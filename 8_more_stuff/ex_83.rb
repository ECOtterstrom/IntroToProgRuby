  # What is exception handling and what problem does it solve?
  
  # Exception handling is the way that the program deals with an
  # error.  Exceptions print out error messages that help programmers
  # find the errors in the code.
  
  # It is a way of handling the error by changeing the flow of control
  # without exiting the program entirely.